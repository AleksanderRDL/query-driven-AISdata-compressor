def _format_value(value: Any) -> str:
    """Format values for a compact markdown table."""
    if value is None:
        return ""
    if isinstance(value, float):
        return f"{value:.4f}"
    return str(value)


def _format_report_table(rows: list[dict[str, Any]]) -> str:
    """Return a compact markdown comparison table."""
    lines = [
        "| " + " | ".join(BENCHMARK_REPORT_TABLE_COLUMNS) + " |",
        "| " + " | ".join(["---"] * len(BENCHMARK_REPORT_TABLE_COLUMNS)) + " |",
    ]
    for row in rows:
        lines.append(
            "| "
            + " | ".join(_format_value(row.get(column)) for column in BENCHMARK_REPORT_TABLE_COLUMNS)
            + " |"
        )
    return "\n".join(lines) + "\n"
